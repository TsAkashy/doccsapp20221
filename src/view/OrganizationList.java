    private void organizationDataBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_organizationDataBtnActionPerformed
        OrganizationDAOImp organizationDAOImp = new OrganizationDAOImp();
        //Se traen los datos de la BD
        try {
            listOrganization = (ArrayList<Organization>) organizationDAOImp.listOrganization();
            Object[] data = new Object[dtmOrganizations.getColumnCount()];
            int i = 1;
            dtmOrganizations.setRowCount(0);
            for (Organization organization : listOrganization) {
                data[0] = organization.getId();
                data[1] = organization.getLegalName();
                data[2] = organization.getComercialName();
                data[3] = organization.getDateRegister();
                data[4] = organization.getType();
                data[5] = organization.isState();
                data[6] = organization.getNotes();
                data[7] = organization.getPhone();
                data[8] = organization.getEmail();
                data[9] = organization.getAddress();
                data[10] = organization.getCity();
                data[11] = organization.getCoutry();
                data[12] = organization.getPostalCode();
                i++;
                dtmOrganizations.addRow(data);
            }

            organizationTbl.setModel(dtmOrganizations);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_organizationDataBtnActionPerformed

    private void organizationDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_organizationDeleteBtnActionPerformed
        int index = organizationTbl.getSelectedRow();
        Organization organization = listOrganization.get(index);
        //JOptionPane.showMessageDialog(null, organization.getId());
        OrganizationDAOImp organizationDAOImp = new OrganizationDAOImp();
        int rta = JOptionPane.showConfirmDialog(null, "Do you want to delete the record?",
                "Remove Organization Registry", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
        if (rta == 0) {
            try {
                organizationDAOImp.deleteOrganization(organization);
                JOptionPane.showMessageDialog(null, "Deleted Organization Record");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Operation Not Performed");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Operation Not Performed");
        }
        if (dtmOrganizations.getRowCount() > 0) {
            for (int i = dtmOrganizations.getRowCount() - 1; i > -1; i--) {
                dtmOrganizations.removeRow(i);
            }
            try {
                var organizations = organizationDAOImp.listOrganization();
                Object[] data = new Object[dtmOrganizations.getColumnCount()];
                int i = 1;
                dtmOrganizations.setRowCount(0);
                for (Organization c : organizations) {
                    data[0] = c.getId();
                    data[1] = c.getLegalName();
                    data[2] = c.getComercialName();
                    data[3] = c.getDateRegister();
                    data[4] = c.getType();
                    data[5] = c.isState();
                    data[6] = c.getNotes();
                    data[7] = c.getPhone();
                    data[8] = c.getEmail();
                    data[9] = c.getAddress();
                    data[10] = c.getCity();
                    data[11] = c.getCoutry();
                    data[12] = c.getPostalCode();
                    i++;
                    dtmOrganizations.addRow(data);
                }
                organizationTbl.setModel(dtmOrganizations);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
                                                   

    
    }//GEN-LAST:event_organizationDeleteBtnActionPerformed

